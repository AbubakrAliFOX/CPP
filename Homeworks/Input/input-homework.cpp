#include <iostream>
using namespace std;

int main () {
    short int A,B;
    cout << "Please enter 1st Number ";
    cin >> A;

    cout << "Please enter 2nd Number ";
    cin >> B;

    cout << "A + B = " << A + B << endl;
    cout << "A - B = " << A - B << endl;
    cout << "A * B = " << A * B << endl;
    cout << "A / B = " << A / B << endl;
    cout << "A % B = " << A % B << endl;
    
    return 0;
}